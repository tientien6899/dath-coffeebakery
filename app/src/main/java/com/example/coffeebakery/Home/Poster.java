package com.example.coffeebakery.Home;

public class Poster {
    String name, link;

    public Poster() {
    }

    public Poster(String name, String link) {
        this.name = name;
        this.link = link;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}
